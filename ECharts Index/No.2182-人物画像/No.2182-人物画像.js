function FloatMul(arg1, arg2, precision) {
    if (arg1 == null || arg2 == null) {
        return 0.0;
    }
    var m = 0,
        s1 = arg1.toString(),
        s2 = arg2.toString();
    if (s1.indexOf('.') != -1) {
        m += s1.split('.')[1].length;
    }
    if (s2.indexOf('.') != -1) {
        m += s2.split('.')[1].length;
    }
    var result = (Number(s1.replace('.', '')) * Number(s2.replace('.', ''))) / Math.pow(10, m);
    if (precision != null) {
        return result.toFixed(precision);
    } else {
        return result;
    }
}
