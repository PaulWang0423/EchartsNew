	function dataFormatter(obj) {

	    var temp;
	    for (var year = 2002; year <= 2011; year++) {
	        var max = 0;
	        var sum = 0;
	        temp = obj[year];
	        for (var i = 0, l = temp.length; i < l; i++) {
	            max = Math.max(max, temp[i]);
	            sum += temp[i];
	            obj[year][i] = {
	                name: myData[i],
	                value: temp[i]
	            }
	        }
	        obj[year + 'max'] = Math.floor(max / 100) * 100;
	        obj[year + 'sum'] = sum;
	    }
	    return obj;
	}

