function redGreenTableLineVisualMap(source, type) {
    console.log(source, type)
    //  now  ~ 60 day
    //   const start = (source.findIndex(item => item.date === dayjs().format('YYYY-MM-DD')) / source.length) * 100
    //   const end = (source.findIndex(item => item.date === dayjs().add(60, 'day').format('YYYY-MM-DD')) / source.length) * 100

    const xAxisData = []
    const seriesData = []
    const realityData = []
    const visualMapPieces = []
    const markPointData = []
    source.forEach((item, i) => {
        xAxisData.push(item.date)
        seriesData.push(item.rpsPlan)
        realityData.push(item.rps)
        visualMapPieces.push({
            gte: i,
            lt: i + 1,
            color: item.color
        })
        if (item.bingoDate === item.date) markPointData.push({
            xAxis: i,
            yAxis: item.rpsPlan
        })
    })
    return {
        dataZoom: [{
            type: 'inside',
            start: 0,
            end: 100
        }, {}],
        // 
        tooltip: {
            trigger: 'axis',
            animation: false,
            axisPointer: {
                type: 'cross'
            },
            formatter(params) {
                const sourceItem = source.find(item => item.date === params[params.length - 1].name)
                const dotHtml = `<span style="display:inline-block;margin-right:5px;border-radius:10px;width:10px;height:10px;background-color:${sourceItem.color}"></span>`
                return `${dotHtml} ${sourceItem.date}
        </br>RPS${sourceItem.rpsPlan}
        </br>RPS${sourceItem.rps}
        </br>()${sourceItem.inventory}
        </br>FBA${sourceItem.fba}
        </br>${sourceItem.arrivalPlan}
        </br>${sourceItem.today}
        </br>${type === 'replenish' ? '' : 'FBA'} ${sourceItem.arrivalDate}`
            },
            position(pos, params, el, elRect, size) {
                const obj = {
                    top: 10
                }
                obj[['left', 'right'][+(pos[0] < size.viewSize[0] / 2)]] = 100
                return obj
            },
            textStyle: {
                align: 'left'
            }
        },
        xAxis: {
            type: 'category', // XcategoryYvalue   //
            boundaryGap: false, //   //
            data: xAxisData
        },
        yAxis: {
            type: 'value',
            splitLine: {
                show: false
            },
            axisPointer: {
                snap: true
            }
        },
        visualMap: {
            type: 'piecewise',
            smooth: true,
            show: false,
            dimension: 0,
            pieces: visualMapPieces,
            splitNumber: 100,
            seriesIndex: 0
        },
        series: [{
                type: 'line',
                smooth: true,
                data: seriesData,
                symbol: 'none',
                sampling: 'average',
                showAllSymbol: false,
                showSymbol: false,
                animation: false,
                smoothMonotone: 'x',
                markPoint: {
                    data: markPointData,
                    symbol: 'path://M512 124L64 900h896z',
                    symbolSize: 10,
                    itemStyle: {
                        color: type === 'replenish' ? '#ff9800' : 'red'
                    },
                    symbolOffset: [0, '-100%']
                },
                markLine: {
                    silent: true,
                    data: [{
                            yAxis: source[0].upper,

                        },
                        {
                            yAxis: source[0].lower
                        }
                    ],
                    lineStyle: {
                        color: 'gray'
                    }
                },
                itemStyle: {
                    normal: {
                        show: true
                    }
                }
            },
            // 
            {
                type: 'line',
                smooth: true,
                data: realityData,
                symbol: 'none',
                sampling: 'average',
                showAllSymbol: false,
                showSymbol: false,
                animation: false,
                smoothMonotone: 'x',
                lineStyle: {
                    normal: {
                        color: 'blue'
                    }
                }
            }
        ]
    }
}
