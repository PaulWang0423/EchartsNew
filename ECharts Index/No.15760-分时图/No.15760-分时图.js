function formatDate(minStr,hourStr){
    if(minStr == 60) {
        minStr = 0;
        hourStr++;
    }
    if(hourStr == 24) {
        hourStr = 0;
    }
    if(minStr>=0 && minStr < 10){
        minStr = '0' + +minStr;
    }
    if(hourStr>=0 && hourStr < 10){
        hourStr = '0' + +hourStr;
    }
    return [hourStr,minStr];
}
function expendDate(date) {
    let hourStr = +date.split(':')[0];
    let minStr = +date.split(':')[1];
    let timeArr = [];
    for(let i =0;i<30;i++){
        minStr++;
        hourStr = formatDate(minStr,hourStr)[0];
        minStr = formatDate(minStr,hourStr)[1];
        
        timeArr.push(`${hourStr}:${minStr}`);
    }
    return timeArr;
}
