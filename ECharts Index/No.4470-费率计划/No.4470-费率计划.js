function GeneratorOptionGrid(length) {
  let grid = []
  let tempLeft = padding
  for (let i = 0; i < length; i++) {
    tempLeft = tempLeft * i == 0 ? tempLeft : tempLeft + margin + width
    grid.push({
      left: tempLeft,
      width: width,
      top: '80',
      bottom: '20',
      containLabel: true
    })
  }
  console.log(grid)
  return grid
}

